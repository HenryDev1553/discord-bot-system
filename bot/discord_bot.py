    @app_commands.command(name="booking_status", description="Ki·ªÉm tra tr·∫°ng th√°i booking")
    async def booking_status(self, interaction: discord.Interaction, email: str = None):
        """
        Slash command ƒë·ªÉ ki·ªÉm tra tr·∫°ng th√°i booking
        """
        try:
            await interaction.response.defer()
            
            if email:
                # T√¨m booking theo email
                data = self.sheets_manager.get_sheet_data()
                bookings = []
                
                for i, row in enumerate(data[1:], start=2):
                    if len(row) >= 2 and row[1].lower() == email.lower():
                        bookings.append({
                            'row': i,
                            'data': row
                        })
                
                if not bookings:
                    await interaction.followup.send(f"‚ùå Kh√¥ng t√¨m th·∫•y booking n√†o cho email: {email}")
                    return
                
                # Hi·ªÉn th·ªã danh s√°ch bookings
                embed = discord.Embed(
                    title=f"üìã Booking c·ªßa {email}",
                    color=0x0099FF
                )
                
                for booking in bookings[-5:]:  # Ch·ªâ hi·ªÉn th·ªã 5 booking g·∫ßn nh·∫•t
                    row = booking['data']
                    status = row[7] if len(row) > 7 else "Ch·ªù x·ª≠ l√Ω"
                    
                    embed.add_field(
                        name=f"üìÖ {row[4]} - {row[5]}",
                        value=f"üè¢ {row[6]}\nüîÑ {status}",
                        inline=True
                    )
                
                await interaction.followup.send(embed=embed)
            
            else:
                # Hi·ªÉn th·ªã th·ªëng k√™ t·ªïng quan
                data = self.sheets_manager.get_sheet_data()
                
                if len(data) < 2:
                    await interaction.followup.send("‚ùå Kh√¥ng c√≥ d·ªØ li·ªáu booking")
                    return
                
                total = len(data) - 1  # Tr·ª´ header
                confirmed = 0
                cancelled = 0
                pending = 0
                
                for row in data[1:]:
                    if len(row) > 7:
                        status = row[7].lower()
                        if 'x√°c nh·∫≠n' in status:
                            confirmed += 1
                        elif 'h·ªßy' in status:
                            cancelled += 1
                        else:
                            pending += 1
                    else:
                        pending += 1
                
                embed = discord.Embed(
                    title="üìä Th·ªëng k√™ Booking",
                    color=0x0099FF
                )
                
                embed.add_field(name="üìù T·ªïng s·ªë", value=str(total), inline=True)
                embed.add_field(name="‚úÖ ƒê√£ x√°c nh·∫≠n", value=str(confirmed), inline=True)
                embed.add_field(name="‚ùå ƒê√£ h·ªßy", value=str(cancelled), inline=True)
                embed.add_field(name="‚è≥ Ch·ªù x·ª≠ l√Ω", value=str(pending), inline=True)
                
                await interaction.followup.send(embed=embed)
                
        except Exception as e:
            logger.error(f"Error in booking_status command: {e}")
            await interaction.followup.send(f"‚ùå L·ªói: {str(e)}")
    
    @app_commands.command(name="refresh_booking", description="L√†m m·ªõi m·ªôt booking t·ª´ Google Sheets")
    async def refresh_booking(self, interaction: discord.Interaction, row_number: int):
        """
        Slash command ƒë·ªÉ l√†m m·ªõi booking t·ª´ Google Sheets
        """
        try:
            await interaction.response.defer()
            
            data = self.sheets_manager.get_sheet_data()
            
            if row_number < 2 or row_number > len(data):
                await interaction.followup.send(f"‚ùå S·ªë d√≤ng kh√¥ng h·ª£p l·ªá: {row_number}")
                return
            
            row = data[row_number - 1]
            
            if len(row) < 6:
                await interaction.followup.send(f"‚ùå D·ªØ li·ªáu d√≤ng {row_number} kh√¥ng ƒë·∫ßy ƒë·ªß")
                return
            
            # T·∫°o booking data
            booking_data = {
                'row_number': row_number,
                'email': row[1] if len(row) > 1 else "",
                'name': row[2] if len(row) > 2 else "",
                'phone': row[3] if len(row) > 3 else "",
                'date': row[4] if len(row) > 4 else "",
                'time': row[5] if len(row) > 5 else "",
                'room': row[6] if len(row) > 6 else "",
                'note': row[8] if len(row) > 8 else ""
            }
            
            # X·ª≠ l√Ω nh∆∞ booking m·ªõi
            await self.process_new_booking(booking_data)
            
            await interaction.followup.send(f"‚úÖ ƒê√£ l√†m m·ªõi booking d√≤ng {row_number}")
            
        except Exception as e:
            logger.error(f"Error in refresh_booking command: {e}")
            await interaction.followup.send(f"‚ùå L·ªói: {str(e)}")

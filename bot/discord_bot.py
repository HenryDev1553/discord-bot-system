    async def _handle_booking_action(self, interaction: discord.Interaction, status: str, title: str, color: int):
        """
        X·ª≠ l√Ω chung cho t·∫•t c·∫£ c√°c action booking
        
        Args:
            interaction: Discord interaction
            status: Tr·∫°ng th√°i m·ªõi ('confirmed', 'cancelled', 'error')
            title: Ti√™u ƒë·ªÅ embed
            color: M√†u embed
        """
        try:
            await interaction.response.defer()
            
            # L·∫•y th√¥ng tin admin
            admin_name = interaction.user.display_name
            admin_id = interaction.user.id
            
            logger.info(f"Admin {admin_name} ({admin_id}) setting booking status to {status} for {self.booking_data.get('email')}")
            
            # Validate rowNumber
            row_number = self.booking_data.get('rowNumber')
            if not row_number:
                logger.error(f"Missing rowNumber in booking data: {self.booking_data}")
                await interaction.followup.send("‚ùå L·ªói: Kh√¥ng t√¨m th·∫•y s·ªë d√≤ng booking!", ephemeral=True)
                return
            
            logger.info(f"Updating row {row_number} to status {status}")
            
            # C·∫≠p nh·∫≠t tr·∫°ng th√°i trong Google Sheets
            success = self.sheets_manager.update_booking_status(
                row_number,
                status,
                admin_name
            )
            
            if not success:
                logger.error(f"Failed to update booking status for row {row_number}")
                await interaction.followup.send("‚ùå L·ªói khi c·∫≠p nh·∫≠t Google Sheets!", ephemeral=True)
                return
            
            # G·ª≠i email t∆∞∆°ng ·ª©ng v√† x·ª≠ l√Ω calendar event
            email_sent = False
            calendar_event_handled = True
            
            if status == 'confirmed':
                email_sent = self.email_manager.send_confirmation_email(self.booking_data)
                # T·∫°o calendar event khi x√°c nh·∫≠n
                try:
                    event_id = self.sheets_manager.add_to_google_calendar(self.booking_data)
                    if event_id:
                        logger.info(f"‚úÖ Created calendar event {event_id} for confirmed booking")
                        # L∆∞u event_id v√†o booking_data ƒë·ªÉ c√≥ th·ªÉ x√≥a sau n√†y
                        self.booking_data['calendar_event_id'] = event_id
                    else:
                        logger.warning("‚ùå Failed to create calendar event for confirmed booking")
                        calendar_event_handled = False
                except Exception as e:
                    logger.error(f"Error creating calendar event: {e}")
                    calendar_event_handled = False
                    
            elif status == 'cancelled':
                email_sent = self.email_manager.send_cancellation_email(self.booking_data)
                # Note: Calendar event c·∫ßn ƒë∆∞·ª£c x√≥a manual b·ªüi admin
                logger.info("üìÖ Note: Calendar event (if exists) should be deleted manually")
                    
            elif status == 'error':
                # C√≥ th·ªÉ g·ª≠i email th√¥ng b√°o l·ªói ho·∫∑c kh√¥ng g·ª≠i g√¨
                email_sent = True  # Gi·∫£ s·ª≠ kh√¥ng c·∫ßn g·ª≠i email cho l·ªãch l·ªói
            
            # C·∫≠p nh·∫≠t message Discord
            embed = discord.Embed(
                title=title,
                color=color,
                timestamp=datetime.now(pytz.timezone(Config.TIMEZONE))
            )
            
            embed.add_field(name="üë§ Kh√°ch h√†ng", value=self.booking_data.get('name'), inline=True)
            embed.add_field(name="üìß Email", value=self.booking_data.get('email'), inline=True)
            embed.add_field(name="üìû ƒêi·ªán tho·∫°i", value=self.booking_data.get('phone'), inline=True)
            embed.add_field(name="üìÖ Ng√†y", value=self.booking_data.get('date'), inline=True)
            
            # Format th·ªùi gian
            start_time = self.booking_data.get('startTime', '')
            end_time = self.booking_data.get('endTime', '')
            time_display = f"({start_time} - {end_time})" if start_time and end_time else self.booking_data.get('time', 'N/A')
            embed.add_field(name="‚è∞ Th·ªùi gian", value=time_display, inline=True)
            
            embed.add_field(name="üè¢ Ph√≤ng", value=self.booking_data.get('room'), inline=True)
            embed.add_field(name="üë®‚Äçüíº X·ª≠ l√Ω b·ªüi", value=f"{admin_name}", inline=True)
            
            # Status-specific fields
            if status in ['confirmed', 'cancelled']:
                embed.add_field(name="üìß Email g·ª≠i", value="‚úÖ Th√†nh c√¥ng" if email_sent else "‚ùå Th·∫•t b·∫°i", inline=True)
            
            # Tr·∫°ng th√°i mapping
            status_display = {
                'confirmed': "**ƒê√É X√ÅC NH·∫¨N**",
                'cancelled': "**ƒê√É H·ª¶Y**", 
                'error': "**L·ªäCH L·ªñI**"
            }
            embed.add_field(name="üîÑ Tr·∫°ng th√°i", value=status_display.get(status), inline=True)
            
            # Disable t·∫•t c·∫£ buttons
            for item in self.children:
                item.disabled = True
            
            await interaction.edit_original_response(embed=embed, view=self)
            
            # G·ª≠i log message
            log_msg = f"{title}\n"
            log_msg += f"üë§ Kh√°ch: {self.booking_data.get('name')}\n"
            log_msg += f"üìß Email: {self.booking_data.get('email')}\n"
            log_msg += f"üìÖ L·ªãch: {self.booking_data.get('date')} - {time_display}\n"
            log_msg += f"üë®‚Äçüíº B·ªüi: {admin_name}"
            
            await interaction.followup.send(log_msg)
            
            logger.info(f"Booking {status} processed successfully for {self.booking_data.get('email')}")
            
        except Exception as e:
            logger.error(f"Error handling booking action {status}: {e}")
            await interaction.followup.send(f"‚ùå L·ªói khi x·ª≠ l√Ω: {str(e)}", ephemeral=True)


